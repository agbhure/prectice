
public class primenum {
	public static void main(String[] args) {
		int n=4;
		int count=0;
		int i=2;
		while(i<=n) {
			if(n%2==00) {
				count++;
				
				
			}
			i++;
		}
		if(count==0) {
			System.out.println(n+"the given number is prime number");
		}
		else {
			System.out.println(n+"the given number is not prime number");
	}

}
}
